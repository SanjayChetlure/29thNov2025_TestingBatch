package S1_Loops;
public class ForLoop6_PrintEvenNumFrom_8_to_2
{
    //startNum - 8
    //endNum   - 2
    //incr/decr - decr by 2

    public static void main(String[] args) {

        //
        for(int i=8; i>=2; i=i-2)
        {
            System.out.println(i);        //8 6 4 2
        }
    }
}
