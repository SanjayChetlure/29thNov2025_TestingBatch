package S1_Loops;
public class ForLoop1_printNumFrom1_to_5
{
    public static void main(String[] args)
    {
//        startNum -> 1
//        endNu    -> 5
//        incr/decr -> incr by 1

//                   6<=5    1+1=2+1=3+1=4+1=5+1=6
        for(int i=1; i<=5; i++)
        {
            System.out.println(i);     //1 2 3 4 5
        }
    }
}
