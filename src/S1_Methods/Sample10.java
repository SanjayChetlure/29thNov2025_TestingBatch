package S1_Methods;
public class Sample10
{
    //method with 2 int parameter
    public static void sub(int num1, int num2)     //num1=80, num2=65
    {
        System.out.println(num1-num2);            //80-65 =15
    }


    //method with 2 int parameter
    public void div(int num1, int num2)     //num1=60, num2=20
    {
        System.out.println(num1/num2);            //60/20=3
    }
}
