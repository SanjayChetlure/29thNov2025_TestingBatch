package S1_Variable;

public class Demo2
{
    public static void main(String[] args)
    {
        //Step1: variable declaration
        String studentName;                          // dataType variableName
        int studentRollNum;
        float studentPer;
        char studentGrade;

        //Step2: variable initialization
        studentName = "Shivani";                          //variableName=variable value
        studentRollNum = 101;
        studentPer=60.2f;
        studentGrade='A';

        //step3: variable usage
        System.out.println(studentName);
        System.out.println(studentName);     //variable re-usage
        System.out.println(studentRollNum);
        System.out.println(studentPer);
        System.out.println(studentGrade);

    }
}
