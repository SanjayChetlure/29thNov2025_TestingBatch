package S3_LogicalProgram;
public class Ex1_EvenOdd
{
    public static void main(String[] args)
    {
        int num=20;

        //20%2= 0==0
        if (num%2==0)     //
        {
            System.out.println("given number is even");
        }
        else
        {
            System.out.println("given number is odd");
        }
    }
}
