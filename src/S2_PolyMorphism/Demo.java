package S2_PolyMorphism;

public class Demo
{
    public void m1()             //non-static method declaration
    {
        System.out.println("non-static method body");     //non-static method definition/body
    }

    public static void m2()      //static method declaration
    {
        System.out.println("static method body");      //static method definition/body
    }

}
